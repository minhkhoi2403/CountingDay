// Love Days Counter Logic (runs on love page if elements exist)
(function initLoveDaysCounter(){
    const daysEl = document.getElementById('love-days');
    const hoursEl = document.getElementById('love-hours');
    const minutesEl = document.getElementById('love-minutes');
    const startDateInput = document.getElementById('start-date-input');
    const startDateDisplay = document.getElementById('start-date-display');
    const saveBtn = document.getElementById('save-date');
    if (!daysEl || !hoursEl || !minutesEl) return;

    const STORAGE_KEY = 'loveStartDate';
    const DEFAULT_DATE = '2023-05-16'; // yyyy-mm-dd
    const toYmd = (d) => {
        const y = d.getFullYear();
        const m = String(d.getMonth()+1).padStart(2,'0');
        const dd = String(d.getDate()).padStart(2,'0');
        return `${y}-${m}-${dd}`;
    };
    let fromStorage = localStorage.getItem(STORAGE_KEY);
    if (!fromStorage) {
        localStorage.setItem(STORAGE_KEY, DEFAULT_DATE);
        fromStorage = DEFAULT_DATE;
    }
    const initial = new Date(fromStorage);
    if (startDateInput) startDateInput.value = toYmd(initial);
    if (startDateDisplay) {
        const d = initial;
        startDateDisplay.textContent = `${String(d.getDate()).padStart(2,'0')}-${String(d.getMonth()+1).padStart(2,'0')}-${d.getFullYear()}`;
    }

    function update(){
        const startStr = startDateInput ? startDateInput.value : toYmd(initial);
        const start = new Date(startStr);
        const now = new Date();
        const diffMs = now - start;
        const totalMinutes = Math.max(0, Math.floor(diffMs / (1000*60)));
        const totalHours = Math.floor(totalMinutes / 60);
        const totalDays = Math.floor(totalHours / 24);
        daysEl.textContent = String(totalDays);
        hoursEl.textContent = String(totalHours % 24);
        minutesEl.textContent = String(totalMinutes % 60);
    }

    update();
    const timer = setInterval(update, 60 * 1000);
    window.addEventListener('beforeunload', () => clearInterval(timer));

    // Date input removed from UI; users keep default date
})();

